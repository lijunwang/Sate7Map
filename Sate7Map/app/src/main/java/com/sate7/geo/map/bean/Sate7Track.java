package com.sate7.geo.map.bean;

public class Sate7Track {
    private String name;
    private String date;

    public Sate7Track(String name, String date) {
        this.name = name;
        this.date = date;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
